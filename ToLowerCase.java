/*Java program for "To Lower Case"*/
public class ToLowerCase {
    // Main method
    public static void main(String[] args) {
        String s="vEnkYs.Io";
        System.out.println("Original String-"+s);
        //converting uppercase characters in string to lowercase characters
        String final_str=s.toLowerCase();
        System.out.println("Resultant String-"+final_str);
    }
}
